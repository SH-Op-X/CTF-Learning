def idct_numpy(a1):
    """
    使用 NumPy 内置的 IDCT 函数恢复 a2
    :param a1: DCT 变换后的数组
    :return: 恢复的原始数组 a2
    """
    a2 = idct(a1, norm='ortho')  # 使用正交归一化
    a2 = np.round(a2).astype(int)  # 四舍五入并转换为整数
    return a2

